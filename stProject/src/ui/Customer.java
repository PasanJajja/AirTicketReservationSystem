                pst1 = con.prepareStatement(sql1);
                //here we are 
                pst.setDate(1, sqlDepTime);
                pst.setString(2, originDB);
                pst.setString(3, destinationDB);
                
                pst1.setDate(1, sqlArrivalTime);
