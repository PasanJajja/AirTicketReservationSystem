    private void reserveTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reserveTicketActionPerformed
        final JPanel panel = new JPanel();
        int dialogResult = JOptionPane.showConfirmDialog(panel, "Do you want to prceed with the reservation.", "Reservation Pending", JOptionPane.YES_NO_OPTION);
        
        if(dialogResult == JOptionPane.YES_OPTION){
            if(onewayTB.isSelected())
                flightNameR = null;
        
            System.out.println(packageName);
            String sql = "INSERT INTO ticket(username, departFlight, returnFlight, noOfTickets, packageName, price) VALUES (?,?,?,?,?,?)";
            try{
                pst = con.prepareStatement(sql);
            
                pst.setString(1, username);
                pst.setString(2, flightNameD);
                pst.setString(3, flightNameR);
                pst.setInt(4, not);
                pst.setString(5, packageName);
                pst.setDouble(6, totalTicketPrice);
            
                pst.execute();
            }catch(SQLException e){
                System.out.println(e);
            }
            final JPanel panel1 = new JPanel();
            JOptionPane.showMessageDialog(panel1, "Your ticket reservation successful, Thank you for flying with us!.", "Reserve Successful", JOptionPane.PLAIN_MESSAGE);
            
            //remove jpanel(s)
            mainPanel.removeAll();
            mainPanel.repaint();
            mainPanel.revalidate();
        
            //add jpanel
            mainPanel.add(searchPanel);
            mainPanel.repaint();
            mainPanel.revalidate();
        }
    }//GEN-LAST:event_reserveTicketActionPerformed

